  students (
    student_id serial primary key,
    first_name varchar(50) not null,
    last_name varchar(50) not null,
    age int,
    grade char(2),
    course varchar(50),
    email varchar(50) unique,
    dob date,
    blood_group varchar(5),
    country varchar(5)
  );

